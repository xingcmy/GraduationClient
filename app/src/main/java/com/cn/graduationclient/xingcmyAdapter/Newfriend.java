package com.cn.graduationclient.xingcmyAdapter;

public class Newfriend {
    private String id;
    private String msg;

    public Newfriend(String id,String msg){
        this.id=id;
        this.msg=msg;

    }


    public void setId(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }


}
